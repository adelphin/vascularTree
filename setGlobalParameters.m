function setGlobalParameters(G, nameNewEdge, visc, deltaP, rotParam, circleParam, Lmin)

    global globG
    global globNameNewEdge
    global globVisc 
    global globDeltaP
    global globRotParam
    global globCircleParam
    global globLmin
    
    globG = G;
    globNameNewEdge = nameNewEdge;
    globVisc = visc;
    globDeltaP = deltaP;
    globRotParam = rotParam;
    globCircleParam = circleParam;
    globLmin = Lmin;
    
    
end